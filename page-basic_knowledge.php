  (function($){
    function getHeaderHeight(){
      return $('header').outerHeight() || 0;
    }

    function scrollToHash(hash, addGap = 0){
      if(!hash) return;
      var $target = $(hash);
      if(!$target.length) return;
      var offset = $target.offset().top - getHeaderHeight() - addGap;
      $('html, body').stop().animate({ scrollTop: Math.max(offset, 0) }, 400);
    }

    $(document).on('click','a[href^="#"]',function(e){
      var href = $(this).attr('href');
      if(href.length <= 1) return;
      e.preventDefault();
      scrollToHash(href, 0);
      history.pushState(null, '', href);
    });

    $(function(){
      if(location.hash) {
        setTimeout(function(){ scrollToHash(location.hash, 0); }, 0);
      }
